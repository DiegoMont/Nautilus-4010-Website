        <h2>Magnitudes</h2>
        <p class="parrafo">La magnitud es la característica de un objeto, sustancia o fenómeno físico que se puede definir de forma numérica. Una margnitud se mide con cantidades, que son los estados particulares de las magnitudes.</p>
        <p class="parrafo">Por ejemplo, el volumen es una magnitud. 24 metros cúbicos sería la cantidad de volumen de un objeto, pues es el número específico de esa magnitud en el objeto.</p>
        <p class="parrafo">Existen diversos tipos de magnitudes:</p>
        <table>
          <tr>
            <th>Tipos de magnitudes</th>
            <th>Explicación</th>
          </tr>
          <tr>
            <td>Continuas</td>
            <td>Dan idea de totalidad, sin partes o elementos naturales identificables. Algunas de ellas son: la longitud, el volumen, la masa material, el tiempo, la presión, la fuerza electromotriz, el peso, la temperatura y la velocidad (es posible tener 4.33 gramos de masa o un intervalo de tiempo de 3.33333 segundos).</td>
          </tr>
          <tr>
            <td>Discontinuas</td>
            <td>No cuenta con infinitos valores dentro de cualquier intervalo finito. También se les llama discretas. Estas son, por ejemplo, el número de libros, mesas, sillas, etc. (No puedes tener 1.33 mesas). Se dividen en escalares y continuas.</td>
          </tr>
          <tr>
            <td>Escalares</td>
            <td>No poseen dirección y quedan definidas por un número que expresa su medida; entre ellas, la longitud, el peso, el área, tiempo y volumen. La longitud de un libro puede ser 22 cm.</td>
          </tr>
          <tr>
            <td>Vectoriales</td>
            <td>Estas magnitudes sí poseen dirección y sentido. Es decir, no basta con conocer solo el valor sino que su dirección y sentido también.  Algunas son la velocidad y la fuerza. Por ejemplo, cuando se dice que un auto va a 80 kilómetros por hora, se hace referencia a que en cada hora se recorren 80 kilómetros, pero también se debe mencionar su dirección (vertical, horizontal, 45) y su sentido (abajo, arriba, al oeste, noreste, etc).</td>
